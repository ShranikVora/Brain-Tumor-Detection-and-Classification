Image(1,:) = 0;
Image(:,1) = 0;
structBoundaries = bwboundaries(Image);
xy=structBoundaries{1};% Get n by 2 array of x,y coordinates.
x = xy(:, 2); % Columns.
y = xy(:, 1); % Rows.
leftColumn = min(x);
rightColumn = max(x);
topLine = min(y);
bottomLine = max(y);
width = rightColumn - leftColumn + 1;
height = bottomLine - topLine + 1;
croppedImage = imcrop(Image, [leftColumn, topLine, width, height]);

handles.cropseg = croppedImage;
guidata(hObject,handles);

axes(handles.axes3)
imshow(croppedImage,[]);


set(handles.pushbutton1,'Enable','on');
